package day05_Concatenation;

public class ShippingAddress {

    public static void main(String[] args) {

        String fullName = "Jimmy Joe", buildingNumber = "7925", streetName = "Jones Branch Dr.",
                city = "McLean", state = "VA", zipCode = "20125A";

        System.out.println(fullName + "\n" + buildingNumber + " " + streetName + "\n" + city + ", " + state
                + " " + zipCode);

    }
}
